		  <h3>Relationship with Bahrain Government</h3>
		  <hr/>
		  <p>
		    The Bahrain Press Club (BPC) was <a href="https://web.archive.org/web/20171020060501/http://bna.bh/portal/en/news/506734">set up</a> in May 2012 under an edict by the Bahrain government's <a href="https://en.wikipedia.org/wiki/Information_Affairs_Authority">Information Affairs Authority</a>. It describes itself as "a quasi-government organization for media and communication professionals in the Kingdom of Bahrain." However it is unclear how the BPC is a "quasi-government" organization when it appears to be entirely set up and run by the government.
		  </p>
		  <p>
		    The BPC seeks to be a "private members-only club set up for journalists and communication professionals" giving them exclusive <a href="https://web.archive.org/web/20121020033855/http://bahrainpressclub.org/members/">access</a> to Bahrain government officials and their version of the latest news, and easier access to Bahrain in general. From the looks of it, this appears to be an attempt by the Bahraini government to get closer access to international journalists covering Bahrain by encouraging them to register with the BPC with the carrot of exclusive access.
		  </p>
		  <p>
		    An overseas liaison office of the BPC in Washington DC was <a href="https://web.archive.org/web/20171020061516/http://bna.bh/portal/en/news/512806">set up</a> in June 2012 with much fanfare at the National Press Club. The launch of the liaison office was <a href="https://web.archive.org/web/20171020063308/https://focuswashington.com/2012/06/15/bahrain-press-club-launches-first-overseas-liaison-office-in-washington/">covered by FocusWashington</a>, a website set up by PR firm <a href="qorvis.php">Qorvis Communications</a> which has been working for the Bahrain government since early 2011. The coverage includes an interview of the president of the Information Affairs Authority Shaikh Fawaz bin Mohammed al Khalifa by <a href="https://web.archive.org/web/20121031045427/http://www.qorvis.com/people/chuck-conconi">Chuck Conconi</a>, vice chairman at Qorvis. A <a href="https://www.youtube.com/watch?v=CmaoU7_0rAo">video report</a> about the launch was also posted on the Youtube channel of the Bahrain Press Club, which may also have been voiced by Conconi.
		  </p>
