		  <h3>Relationship with Bahrain Government</h3>
		  <hr/>
		  <p>
		    Bell Pottinger is a major London-based PR agency that has held a number of contracts with the Bahraini government, including <a href="http://www.prweek.com/uk/news/873574/bahrain-passes-brief-bell-pottinger/">one for the Economic Development Board</a> (EDB) since 2009, believed to be worth a seven-figure sum in pounds annually. Bell Pottinger also contracted <a href="qorvis.php">Qorvis Communications</a> to carry out <a href="http://www.documentcloud.org/documents/406325-5483-exhibit-ab-20100806-22.html">public relations work</a> in the United States for Bahrain's Ministry of Foreign Affairs at some point before August 2010.
		  </p>
		  <p>
		    According to an <a href="http://www.independent.co.uk/news/world/middle-east/bahrain-sentences-four-men-to-death-for-killing-two-policemen-2276390.html">article in the Independent</a> newspaper, Bell Pottinger sends out regular emails to journalist, "including getting out the Manama administration's line at the start of the opposition protests." One email described the Bahraini government's "readiness to receive the injured in hospitals" just before it began <a href="https://en.wikipedia.org/wiki/Bahrain_health_worker_trials">detaining doctors</a>.
		  </p>
		  <p>
		    Bell Pottinger came <a href="http://www.prweek.com/uk/news/1057005/Bell-Pottingers-work-Bahrain-Government-spotlight/">under pressure</a> in February 2011 for holding the contracts with Bahrain even after seven protesters had been killed by security forces in Bahrain, and in April Bell Pottinger <a href="http://www.prweek.com/uk/news/1064256/Bell-Pottingers-Bahrain-brief-suspended-amid-countrys-crisis/">suspended</a> some of its contracts including the EDB one. The chairman of the agency said "<a href="http://www.prweek.com/uk/news/1064256/Bell-Pottingers-Bahrain-brief-suspended-amid-countrys-crisis/">most</a>" -- not all -- of the Bahrain contracts had been suspended, and stressed that it was not necessarily a permanent disengagement.
		  </p>
		  <p>
		    Indeed, the disengagement did not last long. In May 2011, Bell Pottinger was awarded a <a href="http://www.documentcloud.org/documents/406923-bahrain-tenders-may2011.html#document/p12/a66758">contract</a> worth US$ 199,000 by Bahrain's Information Affairs Authority for an undefined period of time. In July 2011, the government's holding company Mumtalakat renewed a <a href="http://www.documentcloud.org/documents/406909-bahrain-tenders-jul2011.html#document/p16/a66759">contract</a> with Bell Pottinger, worth US$ 853,000 for a period of one year. In January 2012, Bahrain's Economic Development Board awarded Bell Pottinger with a renewed contract for <a href="http://www.documentcloud.org/documents/406907-bahrain-tenders-jan2012.html#document/p14/a66760">over US$ 10.5 million</a> for a period of two years. And in May 2012 Bell Pottinger's contract with the <a href="http://www.bcdr-aaa.org/">Bahrain Chamber for Dispute Resolution</a> was <a href="http://www.documentcloud.org/documents/408447-bahrain-tender-may2012.html#document/p16/a67354">renewed</a> for a year for about $ 367,000. The information about these contracts have been taken from the website of the government's <a href="http://www.tenderboard.gov.bh/Tendering/AwardedTenders.aspx">Tender Board</a>, and there may be other contracts that are not yet listed.
		  </p>
