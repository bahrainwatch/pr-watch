		  <h3>Relationship with Bahrain Government</h3>
		  <hr/>
		  <p>
		    The BGR Group (Barbour, Griffith & Rogers) is a prominent Washington DC lobbying company founded by current governor of Mississippi <a href="https://en.wikipedia.org/wiki/Haley_Barbour">Haley Barbour</a>. It was hired by  Bahrain's Economic Development in or before June 2011 (when it first registered this work in House <a href="http://www.documentcloud.org/documents/406884-bgr-house-lobbying-reg.html">lobbying disclosure documents</a>). The job, as declared in the disclosure documents, entails providing "guidance and counsel to promote stronger trade relations between U.S. and Bahrain" by lobbying the House of Representatives.
		  </p>
		  <p>
		    For the period covering the second quarter of 2011, BGR <a href="http://www.documentcloud.org/documents/406887-bgr-lobbying-q2-2011.html#document/p1/a66832">declared receiving $50,000</a> for its work related to this contract. All subsequent lobbying disclosures BGR since then have declared receiving less than $5,000 per quarter. However this appears significantly less than the amount listed on the Bahrain government's Tender Board website, which states that in January 2012 BGR was awarded a <a href="http://www.documentcloud.org/documents/406907-bahrain-tenders-jan2012.html#document/p14/a66833">contract worth BD 90,000</a> (US$ 238,500) for an unspecified period of time.
		  </p>
		  <p>
		    It is also worth noting that BGR's work for the Economic Development Board appears to go back well before June 2011. In August 2009, the pro-government Bahraini newspaper Gulf Daily News published an <a href="http://www.documentcloud.org/documents/413375-bgr-30aug2009.html">article about Morris Reid</a>, Managing Director at the BGR Group. The article started by presenting Reid as an independent business expert, but later explained:
		  </p>
		  <blockquote>"Part of Mr Reid's role at BGR Group is increasing diplomatic relations between America and Bahrain by matching up interested American companies with relevant investment opportunities in the country."</blockquote>
		  <p>
		    The same pro-government publishing house printed <a href="http://www.gulfweeklyworldwide.com/Articles.aspx?articleid=24453">another fluff piece</a> in March 2010 about Reid's visit to Bahrain, without explaining the purpose his visit.
		  </p>
