		  <h3>Relationship with Bahrain Government</h3>
		  <hr/>
		  <p>
		    G3 was hired by the Bahrain government's <a href="https://en.wikipedia.org/wiki/Information_Affairs_Authority">Information Affairs Authority</a> in July 2011, according to <a href="http://www.documentcloud.org/documents/406909-bahrain-tenders-jul2011.html#document/p11/a66834">official tender documents</a>, for a sum of &pound;1.5 million (US$ 2,370,000). G3 was tasked with developing a "media campaign to support Bahrain's position in the international community".
		  </p>
		  <p>
		    Lt Gen Sir <a href="https://en.wikipedia.org/wiki/Graeme_Lamb">Graeme Lamb</a> is a retired British army officer who now works as a "special adviser" to G3. He has had a distinguished career in the British Army, having served as the commander of the SAS and Commander Field Army, and held senior military positions in the Western military campaigns in Iraq and Afghanistan. He has been particularly outspoken in the media about his views on the political unrest in Bahrain, without at any point clarifying that his company has been contracted to do PR for the government.
		  </p>
		  <p>
		    Among the pieces written by Graeme Lamb, or about his views on Bahrain published in the media are:
		  </p>
