		<div class="well">
		  <h3>Relationship with Bahrain Government</h3>
		  <hr/>
		  <p>
		    Joe Trippi & Associates is a D.C.-based media firm headed by Democratic political strategist <a href="https://en.wikipedia.org/wiki/Joe_Trippi">Joe Trippi</a>, who managed Howard Dean's 2004 U.S. presidential election campaign. He was <a href="http://www.salon.com/2011/09/01/joe_trippi_bahrain/">hired</a> by Bahrain's <a href="https://en.wikipedia.org/wiki/Information_Affairs_Authority">Information Affairs Authority</a> some time before August 2011, according to <a href="http://www.documentcloud.org/documents/406968-trippi-exhibit-ab-20110822-1.html">government registration documents</a>.
		  </p>
		  <p>
		    In a March 2012 registration document, Trippi <a href="http://www.documentcloud.org/documents/406970-trippi-supplemental-statement-20120331-1.html#document/p4/a66850">described</a> their work for the Bahrain government in the following words:
		  </p>
		  <blockquote>
		    "We have advised on the implementation of the recommendations of the <a href="https://en.wikipedia.org/wiki/Bahrain_Independent_Commission_of_Inquiry">Bahrain Independent Commission of Inquiry</a> (BICI) and have provided strategic council on issues including the importance of human rights training for police and security personnel, the need for transparency and accountability on the part of the government of Bahrain, strategies to engage Bahraini youth in the reconciliation process, encouraging civil discourse and dialogue, and using social media to effectively communicate reforms and progress as part of reconciliation.<br/><br/>
		    We have also engaged with human rights organizations to encourage and convey the government's actions to move all cases of doctors from military courts to civilian courts or professional review - in accordance with international standards."
		  </blockquote>
		  <p>
		    Specifically, this involved <a href="http://www.documentcloud.org/documents/406969-trippi-exhibit-ab-20111219-2.html#document/p4/a66848">making contacts</a> on behalf of the government with likes of <a href="https://en.wikipedia.org/wiki/Vali_Nasr">Vali Nasr</a>, <a href="https://en.wikipedia.org/wiki/Avaaz">Avaaz.org</a> and <a href="https://en.wikipedia.org/wiki/Front_Line_Defenders">Front Line Defenders</a> as declared in December 2011.
		  </p>
		  <p>
		    For this work, the Bahraini government paid Joe Trippi & Associates a <a href="http://www.documentcloud.org/documents/406969-trippi-exhibit-ab-20111219-2.html#document/p1/a66849">retainer fee of $30,000 per month</a>. Of this amount, $15,000 was paid to PR firm <a href="sanitas-international.php">Sanitas International</a> which Trippi hired as a subcontractor.
		  </p>
		  <p>
		    In May 2012, Trippi declared that it had <a href="http://www.documentcloud.org/documents/406971-trippi-supplemental-statement-20120521-3.html#document/p4/a66854">ended its work</a> for the Bahraini government:
		  </p>
		  <blockquote>
		    "Per our agreement with the client, our activities concluded following the delivery of the report of Bahrain's National Commission, which was tasked with tracking the implementation of the BICI recommendations."
		  </blockquote>
