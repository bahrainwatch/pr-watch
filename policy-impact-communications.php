		  <h3>Relationship with Bahrain Government</h3>
		  <hr/>
		  <p>
		    Bahrain Watch does not have any evidence that Policy Impact was hired by the Government of Bahrain, but we believe it is nevertheless worthy of inclusion, as a non-profit linked to Policy Impact has influenced the narrative on Bahrain.
		  </p>
		  <p>
		    Policy Impact Communications is a Washington PR and lobbying company headed by <a href="https://en.wikipedia.org/wiki/William_Nixon">William Nixon</a>, a former speech writer to Ronald Reagan. In March 2011, Policy Impact <a href="https://www.propublica.org/documents/item/392215-bahrain-american-council-1#document/p6">set up</a> a non-profit group named the <a href="https://web.archive.org/web/20120405062835/http://bactoday.org/">Bahrain American Council</a> (BAC) and began operating it from its offices. The unstated, but apparent, aim of the BAC is to push forward the Bahraini government's narrative of the political unrest in Bahrain, contrary to the prevalent narrative in the Western media.
		  </p>
		  <p>
		    The vice-chairman of the BAC, as listed on its website, Ambassador Richard Carlson, is the director of Policy Impact Communications, and the <a href="https://web.archive.org/web/20160327034631/http://bahrainamericancouncil.blogspot.co.uk/2011/10/about-bac.html">BAC's treasurer</a>, Benjamin May, is Policy Impact's COO. Two other board members of the BAC are also partners of separate political consulting and PR firms.
		  </p>
		  <p>
		    The chairman and president of the BAC, Dr. <a href="https://web.archive.org/web/20160605054442/https://en.wikipedia.org/wiki/Al_Khalafalla">Al Khalafalla</a>, is a Canadian of Sudanese origin, whose father serves as a judge in Bahrain's High Civil Court of Appeal. Khalafalla is featured as a pundit on Policy Impact's "<a href="http://www.policyimpact.com/BAC/POD_1/biography.html">Pundit on Demand</a>" service.
		  </p>
		  <p>
		    The BAC website also had, at some point, a listing of the members of its advisory council, but those names have been removed for some reason. See a screenshot of a google-cached version of the page <a href="http://www.documentcloud.org/documents/407541-bac-board-cache.html">here</a>. Among the names it lists are:
		  </p>
		  <ul>
		    <li><a href="https://web.archive.org/web/20160304035416/http://www.shura.bh/en/Council/MPs/2006MP7/Pages/default.aspx">Jamal Fakhro</a>, Deputy Chairman of Bahrain's royally-appointed upper house of parliament</li>
		    <li><a href="https://web.archive.org/web/20130616231904/http://www.investcorp.com/ourpeople/default.aspx?itemid=123">Mohammed Al Shroogi</a>, a businessman and member of the board of directors of the Bahrain government's Economic Development Board.</li>
		    <li><a href="https://en.wikipedia.org/wiki/Ahdeya_Ahmed">Ahdeya Ahmed</a>, a TV personality on Bahrain's state-run television stations, and a former official government spokesperson</li>
		  </ul>
		  <p>An <a href="http://www.documentcloud.org/documents/407538-policy-impact-bac-intern-request.html#document/p1/a66944">intern request</a> for the BAC on the website of Brigham Young University listed Policy Impact vice president Bart Marcois as the contact person.</p>
		  <p>Additionally, questions have been raised about the role of two American Congressmen in defending the Bahraini government, and their connections to Policy Impact.</p>
		  <br/>
		  <h4>Rep. Eni Faleomavaega</h4><br/>
		  <a href="/img/BAC_AlumniClub.png"><img src="/img/BAC_AlumniClub.png"/></a>
		  <p>
		    <i>Above: Rep. Faleomavaega speaking at an event in Bahrain hosted by the Bahrain American Council in October 2011 (<a href="https://web.archive.org/web/20160315045150/http://bactoday.org/Events">Bahrain American Council</a>)</i>
		  </p>
		  <p>
		    Soon after the BAC was formed, U.S. House of Representatives member <a href="https://en.wikipedia.org/wiki/Eni_Faleomavaega">Eni Faleomavaega</a> became very active in publicly supporting the Bahraini regime and condemning pro-democracy protesters even though he had little history of involvement in Middle East issues. He <a href="http://www.documentcloud.org/documents/407535-policyimpact-faleomavaega-gdn-page03.html">was</a> <a href="http://www.documentcloud.org/documents/407655-policy-impact-faleomavaega-gdn-page01.html">feted</a> by Bahrain's local pro-government media.
		  </p>
		  <ul>
		    <li>In March 2011, he submitted a <a href="http://www.documentcloud.org/documents/407653-policy-impact-faleomavaega-crec-2011-03-29-pt1.html#document/p1/a66960">long testimony</a> in the House of Reperesentatives extolling the Al Khalifa regime and condemning Iran for stirring up trouble among Bahrain's Shia population.</li>
		    <li>In May, Faleomavega was defending the regime in <a href="http://www.documentcloud.org/documents/407659-faleomavaega-statement-human-rights-hearing0001-1.html">a testimony</a> submitted to the Tom Lantos Human Rights Commission into the Bahrain unrest and crackdown.</li>
		    <li>In September he <a href="https://web.archive.org/web/20171024122455/http://www.bna.bh/portal/en/news/473139">met with the King's advisor</a> on diplomatic affairs, Mohammed Abdul Ghaffar</li>
		    <li>
		      In October, Faleomavega visited Bahrain, leading a Congressional "<a href="https://web.archive.org/web/20160413090213/http://thehdi.org/international_exchange/bahrain_oct_2011.html">exchange program</a>" delegation visit organized by the Humpty Dumpty Institute. At <a href="https://web.archive.org/web/20171020195951/https://www.bna.bh/portal/en/news/476966">dinner banquets</a> and <a href="http://www.documentcloud.org/documents/407661-policy-impact-faleomavaega-gdn-page05.html">lectures</a> hosted by the Bahrain American Council, he was once again fearmongering about Iran. In his <a href="http://www.documentcloud.org/documents/407661-policy-impact-faleomavaega-gdn-page05.html#document/p1/a66961">lecture</a> he described how protesting youths who burn tyres are actually part of an elaborate Iranian scheme to destroy the GCC:
		      <blockquote>
			"The youth involved claim they were only trying to protest against their political marginalisation. The government recognised they were near communication towers and suspected their real aim was not only to weaken the structure, but to test the response times of security and emergency personnel. This is roughly equivalent to sending fake bombs through air cargo to see how security forces would react."
		      </blockquote>
		    </li>
		  </ul>
		  <p>
		    It was later <a href="https://web.archive.org/web/20171023221136/https://www.propublica.org/article/meet-bahrains-best-friend-in-congress/">revealed</a> that Faleomavaega was a close friend of Policy Impact vice president Bart Marcois, and that Policy Impact had been his second largest organizational donor in 2010. After Faleomavaega's relationship to Policy Impact was revealed, he <a href="https://web.archive.org/web/20171023174547/https://www.propublica.org/article/congressman-unfriends-bahrain">changed his stance</a> on Bahrain, and his unofficial role was soon <a href="https://web.archive.org/web/20171023135942/https://www.propublica.org/article/lobbyist-linked-group-footed-bill-for-rep-burtons-bahrain-trip">taken over</a> by another member of the House of Representatives, <a href="https://en.wikipedia.org/wiki/Dan_Burton">Dan Burton</a>.
		  </p>
		  <br/>
		  <h4>Rep. Dan Burton</h4><br/>
		  <img src="/img/IMG_7591.jpg"/>
		  <p>
		    <i>Above: The BAC's Al Khalafalla meeting with Rep. Dan Burton in Washington "in preparation for Congressman Burton's speech to the University of Bahrain" (<a href="https://web.archive.org/web/20160315045150/http://bactoday.org/Events">Bahrain American Council</a>)</i>
		  </p>
		  <p>
		    In April 2012, the Bahrain American Council <a href="http://www.documentcloud.org/documents/407863-policy-impact-burton-travel-disclosure.html#document/p6/a67064">paid for</a> Republican Congressman Dan Burton and his wife Samia to go on a trip to Bahrain to speak at the University of Bahrain. There was little mention of his speech at the University, however the state and pro-government did make several mentions of his meetings with government officials and royal family members.
		  </p>
		  <p>
		    According to <a href="http://www.documentcloud.org/documents/407863-policy-impact-burton-travel-disclosure.html#p12">his itinerary</a>, he was scheduled to hold separate meetings with the King, the <a href="http://www.documentcloud.org/documents/407840-policy-impact-burton-gdn-page02.html">Crown Prince</a>, and the Prime Minister of Bahrain. Media reports also covered his meetings with the <a href="http://www.documentcloud.org/documents/407843-policy-impact-burton-gdn-page14.html#document/p1/a67039">president of the Information Affairs Authority</a>, the <a href="http://www.documentcloud.org/documents/407839-policy-impact-burton-gdn-page12.html#document/p1/a67031">Foreign Minister</a>, the <a href="http://www.documentcloud.org/documents/407839-policy-impact-burton-gdn-page12.html#document/p1/a67042">Justice Minister</a>, the <a href="http://www.documentcloud.org/documents/407873-policy-impact-burton-gdn-4apr2012.html#document/p1/a67045">Deputy Premier and the Culture Minister</a>. The latter two meetings were held as part of a Congressional delegation that joined Rep Burton in Bahrain, consisting of Eni Faleomavaega, Jim Himes and Marcia Fudge.
		  </p>
		  <p>
		    The itinerary also mentions that Burton and his wife were to have dinner at the home of a "Shaikh Khalid bin Abdullah Al Khalifa". There is more than one person in Bahrain with that name, but it would be very worrying if this were referring to the former director of the <a href="https://en.wikipedia.org/wiki/National_Security_Agency_(Bahrain)">National Security Agency</a> that was held responsible for much of the state torture and abuse following the February 2011 protests.
		  </p>
		  <p>
		    During their stay in Bahrain, Burton and his wife gave a <a href="https://web.archive.org/web/20120511172609/http://www.youtube.com/watch?v=IrUZtn7QOUA&gl=US&hl=en">26-minute interview</a> to the state-run television channel, that was supportive of the Bahraini government. The interviewer, Ahdeya Ahmed, was <a href="http://www.documentcloud.org/documents/407541-bac-board-cache.html">previously listed</a> as one of the members of the Bahrain American Council's advisory board (as already noted above).
		  </p>
		  <center>
		    <iframe width="425" height="355" src="http://www.youtube.com/embed/IrUZtn7QOUA?wmode=transparent&amp;autohide=1&amp;showinfo=0" frameborder="0" allowfullscreen></iframe>
		  </center>
		  <p>
		    After returning from his trip to Bahrain in April, Burton (following in the footsteps of Faleomavaega) made a <a href="http://www.c-spanvideo.org/clip/3596538">9 minute floor speech</a> (<a href="http://www.documentcloud.org/documents/407851-policy-impact-burton-congress-16apr2012.html#document/p1/a67043">transcript</a>) in the U.S. House of Representatives supporting the Bahraini regime, and accusing the pro-democracy protesters of acting on Iran's behest. His speech was <a href="http://www.documentcloud.org/documents/407838-policy-impact-burton-gdn-page01-2.html#document/p1/a67030">duly covered</a> on the front pages of Bahrain's pro-government press.
		  </p>
		  <p>
		    In May 2012, he was on the <a href="http://www.documentcloud.org/documents/407842-policy-impact-burton-gdn-page01.html#document/p1/a67034">hailed on the front pages</a> of the local pro-government press again, after giving a statement to the <a href="https://web.archive.org/web/20120521160904/http://www.bna.bh/portal/en/news/507911">state-run</a> Bahrain News Agency blaming protesters for violence without any mention of the government's use of violence.
		  </p>
		  <p>
		    In August, he participated in the <a href="https://web.archive.org/web/20151110045809/http://tlhrc.house.gov/hearing_notice.asp?id=1236">Tom Lantos Human Rights Commission hearing</a> into the implementation of the BICI report in Bahrain. Burton was one of the few apologists for the Bahraini government at the hearing. When the issue of sectarian gerrymandered electoral districts was brought up, he <a href="https://web.archive.org/web/20130520035915/http://pomed.org/blog/2012/08/pomed-notes-implementation-of-the-bahrain-independent-commission-of-inquiry-report.html/">tried to justify</a> the situation saying that "gerrymandering is a way of life around the world." Not surprisingly, his comments in defence of the Bahraini regime were featured the next day in the pro-government Gulf Daily News under the headline: "<a href="http://www.documentcloud.org/documents/407841-policy-impact-burton-gdn-page16.html#document/p1/a67033">Opposition is mislead US public opinion</a>".
		  </p>
		  <p>
		    As <a href="https://web.archive.org/web/20171023135942/https://www.propublica.org/article/lobbyist-linked-group-footed-bill-for-rep-burtons-bahrain-trip">reported by Salon.com's Justin Elliott</a>, it turns out that the <a href="http://www.documentcloud.org/documents/407863-policy-impact-burton-travel-disclosure.html#document/p6/a67064">$20,966</a> cost of Rep. Burton's visit to Bahrain was <a href="http://www.documentcloud.org/documents/407863-policy-impact-burton-travel-disclosure.html#document/p6/a67064">financed</a> by the Bahrain American Council. Elliott points out that House ethics rules prevent Policy Impact or the Government of Bahrain from funding the travel of member of Congress directly.
		  </p>
		  <p>
		    It also turns out that Policy Impact employees contributed a sum <a href="https://www.propublica.org/documents/item/402693-policy-impact-burton-donations-5936-supplemental#document/p7/a65273">totaling $1,500</a> to Dan Burton in 2009.
		  </p>
