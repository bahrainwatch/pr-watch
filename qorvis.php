		  <h3>Fees and Expenses</h3>
		  <hr/>
		  <p>
		    $40,000 per month in consulting fees, though Qorvis declares only $39,976 in monthly earnings from this contract.  Out of pocket expenses are <a href="http://www.documentcloud.org/documents/406328-5483-exhibit-ab-20110804-32.html#document/p5/a66462">billed separately</a> (such as travel, production, printing, stock imagery, stock video footage, air and ground charges, accommodations and meals).
		  </p>
		  <p>
		    In the one year period between April 2011 and March 2012 Qorvis received $479,731 in consulting fees from Bahrain. There were also $206,950 in expenses over the same period, including <a href="http://www.documentcloud.org/documents/406330-5483-supplemental-statement-20120605-14.html#document/p57/a66463">$87,828</a> for travel and meals.  In total, fees and expenses came to <b>$686,681</b>.
		  </p>
