		  <h3>Relationship with Bahrain Government</h3>
		  <hr/>
		  <p>
		    Qorvis is a major Washington DC PR firm that is now the government of Bahrain's lead PR representative in the US. It started its work for the Bahrain government after being <a href="http://www.documentcloud.org/documents/406325-5483-exhibit-ab-20100806-22.html">subcontracted</a> by British PR company <a href="bell-pottinger.php">Bell Pottinger</a> in August 2010. Until the the start of Bahrain's Arab Spring protests in February 2011, Qorvis's work for Bahrain <a href="http://www.documentcloud.org/documents/406326-5483-supplemental-statement-20101101-11.html#p16">appears to have been limited</a> to a handful of press releases and meetings. However this work suddenly <a href="http://www.documentcloud.org/documents/406327-5483-supplemental-statement-20110601-12.html#p16">gathered pace</a> after February 2011, and in April 2011 Bahrain's Ministry of Foreign Affairs signed a new <a href="http://www.documentcloud.org/documents/406328-5483-exhibit-ab-20110804-32.html#p5">direct contract</a> with Qorvis.
		  </p>
		  <p>
		    Under the terms of the new contract, the Bahrain government was to pay Qorvis a monthly fee of $40,000 per month plus reimbursement for a wide range of out of pocket expenses like transportation, meals, printing costs etc. Based on its <a href="http://www.documentcloud.org/documents/406329-5483-supplemental-statement-20111201-13.html#p17">disclosures</a> in government documents, Qorvis's work involves:
		  </p>
		  <ul>
		    <li>Press releases on PR Newswire (for example, trying to justify an <a href="http://www.documentcloud.org/documents/406329-5483-supplemental-statement-20111201-13.html#document/p22/a66441">armed raid</a> by government forces on the premises of Doctors Without Borders)</li>
		    <li>Placing <a href="">Facebook ads</a></li>
		    <li>Writing <a href="http://www.documentcloud.org/documents/406329-5483-supplemental-statement-20111201-13.html#document/p18/a66438">letters</a> and <a href="http://www.documentcloud.org/documents/406327-5483-supplemental-statement-20110601-12.html#document/p17/a66436">opinion pieces</a> to newspapers on behalf of Bahraini officials (or in the case of Tom Squitieri <a href="http://www.documentcloud.org/documents/406329-5483-supplemental-statement-20111201-13.html#document/p18/a66437">posing as journalists</a>)</li>
		    <li>Arranging for <a href="http://www.documentcloud.org/documents/406329-5483-supplemental-statement-20111201-13.html#document/p25/a66444">meetings</a> in Washington between visiting Bahraini officials and American journalists, thinktanks and government officials. (Qorvis also put together a "<a href="https://www.propublica.org/article/bahraini-youth-in-washington-courtesy-of-american-spinmeisters">youth delegation</a>" to visit Washington).</li>
		    <li>Protecting government members from negative media. When Houda Nonoo, Bahrain's Ambassador to Washington, <a href="http://www.washingtonpost.com/lifestyle/magazine/bahrain-protests-have-complicated-job-for-houda-nonoo-first-jewish-ambassador-from-an-arab-nation/2012/05/22/gJQA2v9SnU_story.html">gave an interview</a> to the Washington Post in May 2012, a prerequsite was that "a representative from Qorvis ... had to be in the room" the whole time."</li>
		    <li>Qorvis provided video coverage for the <a href="http://www.focuswashington.com/2012/06/15/bahrain-press-club-launches-first-overseas-liaison-office-in-washington/">launch</a> of the Bahrain government's <a href="bahrain-press-club.php">Bahrain Press Club</a> liaison office in Washington</li>
		  </ul>
		  <p>It has also been <a href="http://www.huffingtonpost.com/thor-halvorssen/pr-mercenaries-their-dict_b_863716.html">alleged</a> that Qorvis does "<a href="http://www.qorvis.com/services/reputation-management">online reputation management</a>" for the Bahraini government by using social media and creating blogs and websites to influence the narrative -- something that one former insider described as "<a href="http://www.huffingtonpost.com/2011/03/24/lobbyist-mideast-unrest-departures_n_840231.html">black arts</a>".</p>
		  <p>
		    Qorvis is known to have created the following websites (&amp; related social media accounts):
		  </p>
		  <ul>
		    <li><a href="http://bahrainfacts.org/">BahrainFacts.org</a> (registered 10 October 2011): This site mostly consists of Qorvis press releases presented as neutral "facts" and news stories.</li>
		    <li><a href="http://explorebahrain.org/">ExploreBahrain.org</a> (registered 23 November 2011): This site, which appears to have been launched in May 2012, offers a more visually dynamic presentation of news stories about Bahraini culture, business, sports, environment and tourism. It also has an active Twitter account (<a href="https://twitter.com/explore_bahrain">@explore_bahrain</a>) and <a href="https://www.facebook.com/ExploreBahrain">Facebook page</a>, as well as a <a href="https://www.youtube.com/explorebahrain">YouTube channel</a>.</li>
		    <li><a href="http://bahrainstories.com/">BahrainStories.com</a> (registered 9 January 2012): Launched early in 2012, ahead of the 1-year anniversary of February 14th, Bahrain Stories presents itself as a interactive site encouraging inhabitants of Bahrain from multiple nationalities to submit a short personal narrative. The speakers are indeed genuine, however the site is clearly intended to paint a particular picture given the limited range of people - many of whom are in the creative industries - who contribute stories or are approached by Bahrain Stories. The site also has a very active Twitter account (<a href="https://twitter.com/bahrainstories">@bahrainstories</a>) and <a href="https://www.facebook.com/TheBahrainStories">Facebook page</a>.</li>
		  </ul>
		  <p>
		    Qorvis has also registered the domains InfoBahrain.org (on 26 July 2011), EntertainBahrain.com and  ProsperBahrain.com (both on 12 December 2011), however these sites have not gone live. All the above websites were registered with GoDaddy.com, payments to which appear in <a href="http://www.documentcloud.org/documents/406330-5483-supplemental-statement-20120605-14.html#document/p55/a67157">Qorvis's FARA filings</a>.
		  </p>
		  <p>
		    Despite all of the bad press, the government does not hide its relationship to Qorvis. In August 2011, Bahrain's state news agency proudly issued a <a href="http://www.bna.bh/portal/en/news/469737">press release</a> about a meeting between Bahrain's Minister of Industry & Commerce and a Qorvis team led by <a href="http://www.qorvis.com/people/john-reid">John Reid</a>, along with this photo:
		  </p>
		  <img src="/img/Qorvis_0.jpg"/>
		  <p>
		    Similarly, in July 2011, the Bahrain News Agency had a <a href="http://www.bna.bh/portal/news/466824">press release</a> about a pro-government legislator in Bahrain who proudly wrote a letter to American NGOs, government officials and journalists clarifying the situation in the country. Number 55 on the list of recipients of that letter is the president of Qorvis!
		  </p>
		  <br/>
		  <h4>C&amp;O Resources</h4>
		  <br/>
		  <p>
		    Qorvis appears to have hired the services of <a href="http://www.co-resources.net/">C&amp;O Resources</a>, another Washington DC based consulting company, as a subcontractor for its Bahrain account. Qorvis's FARA filings show that C&amp;O Resources has been charging <a href="http://www.documentcloud.org/documents/406329-5483-supplemental-statement-20111201-13.html#document/p57/a67483">a consulting fee</a> of <a href="http://www.documentcloud.org/documents/406330-5483-supplemental-statement-20120605-14.html#document/p54/a67484">$5,000 per month</a> starting in April 2011. The firm focuses on assisting Middle Eastern companies and governments do business with the United States, and was notably <a href="http://www.documentcloud.org/documents/408644-bahrain-fta-luncheon-2004.html#document/p14/a67485">a member</a> of the US-Bahrain Free Trade Agreement Coalition in 2004. The firm incidentally happens to be located at the same address as Qorvis's headquarters, however it seems there is some <a href="https://humanrightstodolist.wordpress.com/2012/06/26/curiouser-and-curiouser-not-lobbyists-for-the-kingdom-of-bahrain/">internal confusion</a> over the nature of the relationship.
		  </p>
